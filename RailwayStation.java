class RailwayStation{

static String placeName;
static int platformNumber;
static int seatNumber;

static void setPlaceName(String pName){
placeName = pName;

}
static String getPlaceName(){
return placeName;
}
static void setPlatformNumber(int pNumber){
platformNumber=pNumber;

}
static int getPlatformNumber(){
return platformNumber;
}
static void setSeatNumber(int sNumber){
seatNumber = sNumber;
}

static int getSeatNumber(){
return seatNumber;
}
}